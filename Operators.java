public class Operators {
    public static void main(String[] args) {
        //operators in java: operators are the symbol used to perform operations {+,-,<,>,!=,==}
        //In a+b a and b are operands and + is the operators
        //1.Arthamatic operation: It perform operation like +,-,*,/
        //Example
        int a=22;
        int b=33;
        int sum=a+b;
        System.out.println("Addition="+sum);
        //sub
         //subtract
         int numc=12;
         int numd=11;
         int sub= numc-numd;
         System.out.println("subtraction="+sub);
        //Multiply
        int e=22;
        int p=33;
        int Multiply=e*p;
        System.out.println("Multiplication="+Multiply);
        //divide
        float w=44f;
        float q=22f;
        float divide= w/q;
        System.out.println("Division="+divide);
        System.out.println("--------------------------------");

        //Relational operators:Compare between two or more than two values
        int c=30;
        int d=40;
        System.out.println("c>d= "+(c>d));
        System.out.println("c>=d= "+(c>=d));
        System.out.println("c<d= "+(c<d));
        System.out.println("c<=d= "+(c<=d));
        System.out.println("c==d= "+(c==d));
        System.out.println("c!=d= "+(c!=d));
        System.out.println("--------------------------------");
        //Logical operator:It operates on boolean valued
        //And=&&,or=||,not=!
        boolean x= true;
        boolean y= false;
        System.out.println("x&&y= "+(x&&y));
        System.out.println("x||y= "+(x||y));
        System.out.println("!x= "+(!x));
        System.out.println("--------------------------------");
        //Assignment operator:It is used to assign value to variable
        //operators: (+=,-=,/=,*=,=,%=)
        int val1=2;
        int val2=3;
        System.out.println("+= = "+(val1+=val2));
        System.out.println("-= = "+(val1-=val2));
        System.out.println("/= = "+(val1/=val2));
        System.out.println("*= = "+(val1*=val2));
        System.out.println("= = "+(val1=val2));
        System.out.println("%= = "+(val1%=val2));
        
    








    }
    
}
